package presintation;

public class GeoRegularPolygon extends GeoShape{
	
	private double length;
	private int sides;
	
	public double getLength() {
		return length;
	}
	public void setLength(double length) {
		this.length = length;
	}
	public int getSides() {
		return sides;
	}
	public void setSides(int sides) {
		this.sides = sides;
	}
	
	

}
